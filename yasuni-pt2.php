<article class="bigtext">
    <p>A capital do Equador, Quito, está a 250 quilômetros da região conhecida como Yasuní, nome de um dos rios que a atravessa. Situada no lado oeste da bacia amazônica, sua proximidade com a cordilheira dos Andes marca as florestas úmidas com uma topografia acidentada por pequenas colinas e desfiladeiros. Elas são interrompidas por riachos e pântanos que formam uma rede hídrica para o trânsito de antas, capivaras, onças e jacarés. As árvores mais altas chegam a 45 metros de altura e se sobressaem ao dossel, área alta onde galhos e folhas se sobrepõem e cuja sombra abriga pântanos e uma vasta vegetação.</p>

    <p>Yasuní tem uma das mais ricas biodiversidades do planeta, ainda longe de ser entendida pelos cientistas. É terra ancestral de grupos indígenas, alguns dos quais em isolamento voluntário. Também é moradia de um número crescente de colonos vindos de outras partes do Equador. Além disso, guarda as mais importantes reservas de petróleo do Equador. A exploração deste recurso ameaça a floresta com a construção de oleodutos e estradas. As obras incentivam a vinda de migrantes e o aumento da população leva ao desmatamento e redução do número de animais silvestres.</p>
