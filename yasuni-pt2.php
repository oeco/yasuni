    <h1>Iniciativa Yasuní ITT</h1>
    
    <p>Em 2007, o governo de Rafael Correa lançou um plano em que se comprometia a não explorar os campos de petróleo Ishpingo-Tambococha-Tiputini (ITT), do lote 43 desde que fosse compensado financeiramente pela comunidade internacional.</p>

    <p>A meta era conseguir 3,6 bilhões de dólares, ou metade do valor estimado das reservas de petróleo dos campos ITT. Após o lançamento oficial, para recolher o dinheiro, criou-se um fundo administrado pelo Programa das Nações Unidas para o Desenvolvimento (PNUD). Os recursos desse fundo seriam destinados a financiar projetos no Equador para proteger a biodiversidade, manter áreas protegidas e restaurar áreas degradadas. Também seriam usados para programas sociais e para o desenvolvimento de fontes de energia renováveis.</p>

    <p>A Iniciativa Yasuní ITT nasceu de grupos ambientalistas preocupados com a crescente poluição causada pela atividade petrolífera nos anos 90 e com a proteção dos direitos dos povos nativos. Os campos ITT estão no extremo leste do Equador, na fronteira com o Peru, e têm uma extensão de 200 mil hectares. Eles representam a quinta parte da superfície total do Parque Nacional Yasuní.</p>

    <p>A Iniciativa ITT contou com o apoio de 15 países, 20 organismos internacionais, 65 governos de províncias e municípios do Equador, além de 39 alianças com setores empresariais domésticos e no exterior. Entretanto, até 2013, obteve promessas de apenas 376 milhões de dólares, cerca de 10% do total almejado.</p>

    <div class="video">
    <iframe src="//player.vimeo.com/video/83624381?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
    </div>

    <p>Após várias extensões da data limite para conseguir os aportes internacionais, em 15 de outubro de 2013, o presidente Rafael Correa deu por encerrada a iniciativa.</p>

    <p>Quando abandonou a Iniciativa Yasuní-ITT, o presidente Rafael Correa afirmou que o mundo tinha faltado ao Equador. Entretanto, durante o tempo em que ela esteve em vigor, o governo continuou com os trabalhos de construção de instalações e infraestrutura para a exploração de petróleo nos campos ITT, o que gerou dúvidas em países interessados, como a Alemanha, se o Equador cumpriria sua parte do plano.</p>

    <p>Após o fracasso do plano, apesar da oposição dos ambientalistas, o governo anunciou o início da exploração nos campos ITT com uma nova promessa: apenas 0,1% do parque nacional será afetado pela extração de petróleo.</p>

    <p>Entretanto, não é possível saber se os efeitos negativos sobre a floresta serão proporcionais á área explorada. A criação de estradas e áreas abertas, por menores que sejam, traz consigo o aumento de assentamentos humanos, caça e extração de madeira, inclusive em áreas controladas.</p>

    <p>A história da iniciativa de receber compensação internacional para parar a exploração de recursos naturais e trocá-la por proteção ambiental transformou Yasuní em um símbolo internacional.</p>

    <p>O fracasso da Iniciativa ITT devolveu aos cidadãos o movimento para proteger Yasuní, que agora reúnem assinaturas da sociedade civil para promover uma consulta popular, em que os cidadãos irão decidir sobre a preservação ou exploração do petróleo nos campos ITT.</p>
</article>

