    <p>Em 1999, o extremo sul do parque nacional foi designado como Zona Intangível para garantir os direitos dos povos Tagaeri-Taromenane, que lá vivem em isolamento voluntário. Em 2007, a Zona Intangível foi delimitada em 758.051 hectares e entregue oficialmente aos indígenas. Nela não há exploração de petróleo, pois por lei é inviolável.</p>
 
