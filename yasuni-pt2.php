<article class="bigtext">
    <p>A região de Yasuní foi reconhecida pelo <a href="http://www.unesco.org/new/pt/brasilia/natural-sciences/environment/biodiversity/biodiversity/mab-programme-in-brazil/"target="_blank">Programa Homem e a Biosfera</a> da UNESCO como Reserva da Biosfera, uma <a href="http://www.unesco.org/mabdb/br/brdir/directory/biores.asp?mode=all&code=ECU+02" target="_blank">área total de 1.682.000 hectares</a>, que inclui o Território Indígena Waorani e o Parque Nacional Yasuní. Na parte sul do Parque Nacional fica a Zona Intangível Tagaeri-Taromenane.</p>

    <p>Até os anos 60 Yasuní era habitada apenas por povos indígenas que viviam isolados. Foi o interesse pela exploração de petróleo que levou o governo do Equador e empresas petrolíferas a buscarem contato com os indígenas. Em seguida, boa parte dessas tribos foi despejada de suas terras.</p>

    <p>Em 1979, sensibilizado pela importância de Yasuní para a proteção ambiental, o governo criou um parque nacional que ocupa uma parcela de 982.000 hectares da região. Em 1983, foram legalizados como terras indígenas waorani 66.500 hectares ao oeste do parque nacional. Em 1989, a região recebeu o status de Reserva da Biosfera. Em 1990, o governo equatoriano decretou um aumento de quase dez vezes da área do território waorani, que passou para 612.560 hectares. A condição foi de que eles não se opusessem à exploração de petróleo e gás dentro deste território.</p>

