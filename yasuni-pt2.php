    <p>O próprio Parque Nacional Yasuní tem uma história conturbada. Em 30 anos suas fronteiras mudaram várias vezes em função das necessidades de exploração de petróleo. Hoje, dentro do parque são operados e ativos os lotes de petróleo 14, 15, 16, 67 (ativos).</p>

    <p>Há outros dois, os blocos 31 e 43, cuja infraestrutura de exploração está em construção. Sozinhos, eles guardam 895 milhões de barris de petróleo, que correspondem a cerca de 12% das reservas do Equador. O bloco 43 contém os campos Ishpingo-Tambococha-Tiputini, conhecidos pela sigla ITT. A proposta do movimento ambiental para uma moratória de exploração de petróleo nos campos ITT deu origem a uma campanha, acolhida pelo governo nacional do país, que parou por cinco anos a exploração do petróleo nestes campos.</p>
</article>

