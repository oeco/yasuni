        <p>Las características ecológicas, la gran variedad de hábitats y la ubicación geográfica hicieron de Yasuní una cuna para el desarrollo de nuevas especies.</p>

        <p>Como explica la doctora en Zoología de la Universidad San Francisco de Quito, Stella de la Torre, los ríos de la región cambian de curso con frecuencia y forman barreras naturales que pueden separar poblaciones de fauna y flora. Ambientes con sutiles diferencias ejercen presiones evolutivas que producen especies muy distintas en las zonas muy cercanas.</p>

        <div class="video">
        <iframe src="//player.vimeo.com/video/82719593?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>Este mosaico de hábitats se formó por el gradiente altitudinal en una región ecuatorial cercana a los Andes, un fenómeno que no se ve en otras regiones de la cuenca amazónica. Yasuní es también una de las regiones amazónicas con mayor precipitación.</p>

        <p>Se estima que contiene más de 4.000 especies de plantas vasculares. Hasta el momento han sido descritas 138 especies de lianas, 313 de epífitas (plantas como bromelias o musgos), 140 helechos y similares, además de 2.113 especies de árboles y arbustos (1.813 identificadas y 300 por identificar). Una investigación encontró 655 especies de árboles en una sola hectárea del Yasuní, la misma cantidad que se encuentra en todos los bosques de América del Norte.</p>

        <blockquote>Una <a href="http://www.plosone.org/article/info:doi/10.1371/journal.pone.0008767" target="_blank">investigación</a> encontró 655 especies de árboles en una sola hectárea en Yasuní, la misma cantidad que se encuentra en todos los bosques de América del Norte.</blockquote>

        <div class="video">
        <iframe src="//player.vimeo.com/video/82104960?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>Se conocen 176 especies de mamíferos, entre los cuales se registran más de 80 especies de murciélagos. 596 especies de aves representan el 40% del total de la cuenca amazónica. Los anfibios y reptiles forman la comunidad de herpetofauna más rica del planeta con 271 especies. Los sistemas fluviales y lacustres (referente a lagos) abrigan 382 especies de peces, cifra que supera el total de toda la cuenca del río Mississippi, una de las tres principales del mundo. La riqueza de insectos es la más alta en el mundo, estudios estiman alrededor de 100 mil especies en una sola hectárea.</p>
