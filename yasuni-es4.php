        <h2>El jaguar (o Kengiwe)</h2>

        <p>El jaguar <i>(Panthera onca)</i> es llamado kengiwe en el lenguaje wao terero de los indígenas waorani. De acuerdo con las creencias, el kengiwe es la reencarnación de un guerrero de la tribu. Después de su muerte, los guerreros regresarían a la selva teñidos de dorado, con cuerpos robustos, grandes cabezas y mandíbulas poderosas.</p>

        <div class="bigimage"><img style="width:700px;" src="img/fauna/jaguar.jpg" alt="Foto: Diego Mosquera">
        <p class="legend"><i>Foto: Diego Mosquera</i></p>
        </div>

        <p>A pesar de la fuerza y el tamaño, por jaguares por instinto se mantienen lejos de la gente. Sin embargo, con cámaras trampa, los científicos los han fotografiado 250 veces en los últimos 8 años. A través de estas imágenes se conoce de la existencia de 21 jaguares en los alrededores de la Estación de Biodiversidad Tiputini, en la Reserva de la Biosfera Yasuní.</p>

        <div class="video">
        <iframe src="//player.vimeo.com/video/82726643?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="700" height="300" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>Los jaguares cazan en el río o en el bosque. Peces, roedores y cerdos salvajes son parte de las 80 especies de animales de las que se alimenta. Su presencia es un indicador ambiental del estado de la selva, ya que la especie necesita abundancia de presas para sobrevivir. Cada individuo necesita de un área aproximada de casi 10.000 hectáreas para encontrar comida, esconderse y reproducirse.</p>

        <p>En más de 8 años de investigación en Yasuní el proyecto cámara trampa de la Universidad San Francisco de Quito permitió a Diego Mosquera, Kelly Swing, David Romo y otros investigadores clasificar casi 55 mil fotografías de 60 especies diferentes, incluyendo mamíferos, aves, anfibios y reptiles. A pesar de que sólo 600 hectáreas han sido estudiadas, y los expertos todavía no han terminado de procesar todos los registros de los animales.</p>

