        <h1>Cacería</h1>

        <p>Durante los últimos 40 años el gran territorio entre los ríos Napo y Cururay se transformó. Las carreteras avanzaron y permitieron el acceso a lugares que antes eran remotos. La cacería de subsistencia por parte de indígenas waorani y kichwa como práctica tradicional, se convierte en una actividad comercial. Se deja de cazar en zonas cercanas a la comunidad porque las carreteras permiten el acceso a bosques a los que no se podía llegar. Las carreteras permiten llegar a nuevos sitios de cacería y facilitan la salida de esa carne silvestre.</p>

        <blockquote>Los indígenas kichwas amazónicos son el grupo de mayor número en la zona de influencia del Parque Nacional Yasuní. Su población creció a lo largo del río Napo, desde la segunda mitad del siglo XIX, debido a la expansión de la industria del caucho.</blockquote>

        <div class="bigimage"><img style="width:800px;" src="img/via_auca/02_via_auca_full.jpg" alt="Foto: Martin R. Bustamante / www.biographica.com.ec">
        <p class="legend"><i>Foto: Martin R. Bustamante / www.biographica.com.ec</i></p>
        </div>
 
        <p>La carne de animales silvestres capturados en Yasuní se comercializa en Pompeya, un pequeño pueblo a orillas del río Napo. En la feria de los sábados se pueden encontrar jabalíes, ciervos, huevos de tortuga y una variedad de peces como el arapaima, sábalos y carachamas, que son vendidos a personas que vienen de las ciudades de Puyo, Tena y Coca.</p>

        <p>Hace años este comercio era más evidente. El control por parte del Ministerio de Medio Ambiente de Ecuador ha llevado a que la actividad sea más discreta, y a que se creen otros lugares de venta como Chiro Isla al norte del parque.</p>

        <p>La carne es apetecida porque tiene un alto valor ritual en la identidad los pobladores nativos de la Amazonia. Para los turistas, es considerada una exquisitez y una experiencia folclórica.</p>

        <div class="video">
        <iframe src="//player.vimeo.com/video/83402862?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <div class="lead">Se estima que cada año se venden ilegalmente 10 toneladas de carne silvestre, principalmente de mamíferos de tamaño mediano, además de aves y peces</div>

        <p>El ecólogo Esteban Suárez, en colaboración con la Wildlife Conservation Society (WCS) y la Universidad San Francisco de Quito, investigó el mercado de Pompeya y logró cuantificar la magnitud del comercio de carne de animales silvestres. Se estima que cada año se venden ilegalmente 10 toneladas principalmente de mamíferos de tamaño mediano, además de aves y peces. "El panorama no es muy alentador si notamos que esa carne es sobre todo de mamíferos que viven en poblaciones con densidades muy bajas, donde el  impacto es fuerte si le añadimos que esta cacería no es para subsistencia y por ende no es sostenible en el largo plazo. Estamos viendo niveles de cacería que van hacer colapsar poblaciones de estos animales en un futuro cercano", dice Suárez.</p>

        <p>Los beneficios se quedan en la ciudad, porque los intermediarios y los restaurantes venden la carne por un valor cinco veces mayor que el precio pagado al cazador. Mientras tanto, los animales de la selva desaparecen.</p>

