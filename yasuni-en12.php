    <article class="bigtext">
        <p>For the Ecuadorian economy, Yasuní is more important for the dollars that rise up from its grounds than for the life forms it shelters. According to data from Ecuador’s Central Bank, in 2012, the four active plots in Yasuní produced 32 million barrels of oil, 17.4% of the total of 184 million barrels produced by the entire country that year.</p>

        <p>According to Eugenio Pappalardo, oil concession areas represent almost 75% of the total area of the Yasuní Biosphere Reserve and include parts of Waorani territory. Only the intangible zone is free from oil drilling activities.</p>

        <p>Contained in the Biosphere Reserve, it is thought that the underground of Yasuní National Park holds 895 million barrels only in ITT fields (43) and in plot 31. According to a 2010 survey, this corresponds to 12.3% of the country’s verified reserves of 7.3 billion barrels.</p>

