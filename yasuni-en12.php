        <p>Human rights activists and the environmentalist movement put up a fight, but not to keep Yasuní forests untouched, for they have lost this status long ago. The struggle is to defend the biodiversity of these forests and the way of life of its native populations.</p>

        <p>An essential part of this quest is to harmonize indigenous people’s rights, whether living in isolation or not, with the activities around Yasuní.</p>

        <p>Yasuní was and will always be a territory of utopias. It springs forth questions on how to avoid the ethnocide of ancestral cultures, distribute the riches generated by the extraction of its natural resources, while preserving nature. In other words, a classic case of having your cake and eating it too. The region’s future depends on practical and functional answers to these questions.</p>
