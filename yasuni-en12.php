        <p>The exploitation of oil in Yasuní unleashes fierce criticisms. Ecuador’s Constitution says extraction activities must not take place within protected areas. It also establishes that present and future generations have a right to the conservation and restoration of nature. According to Ricardo Crespo, former advisor for Ecuador’s National Council Biodiversity Commission, the problem is that the same Constitution also contains article 407 where the expression “of national interest” can be read. This article allows the President to request from the National Council a declaration that oil, ore or wood exploitation is of national interest even if within protected areas.</p>
        
        <div class="video">
            <iframe src="//player.vimeo.com/video/83624381?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>In 2013, President Rafael Correa used article 407 to allow oil exploitation in ITT fields (plot 43). This was done although the government itself, from 2007 to 2013, promoted the need to respect indigenous groups living in isolation and avoid oil drilling. The country’s National Council complied with the President’s request.</p>
        
        <p>The same “green constitution”, as it has already been called, prioritizes cultures, communities, indigenous nations and isolated peoples through an acknowledgement of international instruments for protecting populations contained in article 57 of the United Nation’s Declaration on the Rights of Indigenous Peoples. For this reason, the Ecuadorian Constitution should guarantee the collective rights of indigenous communities and of the peoples living in voluntary isolation, such as the Tagaeri and the Taromenane groups that live inside Yasuní National Park.</p>

