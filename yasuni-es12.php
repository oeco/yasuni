        <p>La exploración petrolera en Yasuní despierta fuertes críticas. La Constitución de Ecuador dice que actividades de extracción no deben ocurrir dentro de las áreas protegidas. También establece que las generaciones presentes y futuras tienen derecho a la conservación y restauración de la naturaleza. Según Ricardo Crespo, exasesor de la Comisión de Biodiversidad de la Asamblea Nacional de Ecuador, el problema es que la misma Constitución también contiene el artículo 407, donde surge la expresión “interés nacional”. Este artículo permite que el presidente de la república solicite a la Asamblea Nacional declarar de interés nacional la explotación de petróleo, la minería o de la madera dentro de las áreas protegidas.</p>
        
        <div class="video">
            <iframe src="//player.vimeo.com/video/83624381?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
