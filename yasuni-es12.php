    <article class="bigtext">
        <p>Para la economía ecuatoriana, Yasuní es más importante para los dólares que fluyen desde el suelo que las formas de vida que lo habitan. Según datos del Banco Central de Ecuador, en 2012 los cuatro lotes activos de Yasuní produjeron 32 millones de barriles de petróleo, 17,4% de los 184 millones de barriles producidos en todo Ecuador en ese año.</p>

        <p>Según Eugenio Pappalardo, las áreas de concesión de petróleo representan casi 75% de la superficie total de la Reserva de la Biosfera Yasuní, e incluyen partes del territorio waorani. Únicamente la zona intangible es libre de la actividad petrolera.</p>

        <p>Incluido en la Reserva de la Biosfera, se estima que el subsuelo del Parque Nacional Yasuní contiene 895 millones de barriles sólo en los campos ITT (43) y en el lote 31. De acuerdo con un estudio de 2010 de la Secretaría de Hidrocarburos de Ecuador, corresponde a 12,3% de las reservas remanentes del país, de 7.3 billones de barriles.</p>

