        <p>En 2013, el presidente Rafael Correa usó el artículo 407 para pedir a la Asamblea Nacional que declare de interés nacional la explotación de petróleo en los campos ITT (lote 43). Esto se hizo a pesar de que fue el mismo gobierno que, desde 2007 a 2013, promovió la necesidad de respetar los grupos indígenas en aislamiento voluntario y evitar la explotación de petróleo en una zona con tanta biodiversidad. La Asamblea Nacional del país aceptó la solicitud del presidente.</p>
        
        <p>La misma “constitución verde”, como ya fue llamada, prioriza las culturas, comunidades, naciones indígenas y los pueblos aislados a través de la ratificación de instrumentos internacionales para la protección de los pueblos que figuran en el artículo 57 de la Declaración de Derechos Indígenas. Por lo tanto, la Constitución debería garantizar los derechos colectivos de las comunidades indígenas y de los pueblos en aislamiento voluntario, como los grupos tagaeri y taromenane que están dentro del Parque Nacional Yasuní.</p>

