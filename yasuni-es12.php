        <h2>Datos sobre el petróleo</h2>

        <p>La producción de los 3 lotes activos dentro del parque fue de casi 32 millones de barriles de petróleo, el 17,4% de la producción nacional actual, cifra que no es exacta, ya que incluye el lote 15, cuya mayor área se encuentra fuera de Yasuní.</p>

