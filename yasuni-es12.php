        <p>Los activistas de derechos humanos y el movimiento ecologista luchan no para mantener los bosques de Yasuní vírgenes, porque hace mucho ya perdieron esta condición. La lucha es por la biodiversidad de estas selvas y la protección de los medios de vida de sus pueblos indígenas.</p>

        <p>Parte esencial de esta lucha es conciliar los derechos de los indígenas, aislados o no, con las actividades en Yasuní.</p>

        <p>Yasuní fue, es y será el territorio de las utopías. De ahí surgen preguntas acerca de cómo evitar el etnocidio de las culturas ancestrales, cómo distribuir la riqueza generada por la explotación de los recursos naturales, y cómo preservar la naturaleza. De respuestas prácticas y funcionales a estas cuestiones depende el futuro de la región.</p>
