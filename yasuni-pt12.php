        <p>Os ativistas de direitos humanos e o movimento ambientalista lutam não por manter as florestas de Yasuní virgens, pois há muito elas perderam essa condição. A briga é pela biodiversidade destas florestas e em defesa dos modos de vida dos seus povos nativos.</p>

        <p>Parte essencial desta busca é conciliar os direitos dos indígenas, isolados ou não, com as atividades no entorno de Yasuní.</p>

        <p>Yasuní foi, é e será território das utopias. Dele emergem perguntas sobre como evitar o etnocídio de culturas ancestrais, distribuir a riqueza gerada pela exploração dos recursos naturais e preservar a natureza. De respostas práticas e funcionais a essas questões depende o futuro da região.</p>
