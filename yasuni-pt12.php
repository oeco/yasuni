        <p>Para a economia equatoriana, Yasuní é mais importante pelos dólares que brotam do chão do que pelas formas de vida que abriga. De acordo com dados do Banco Central do Equador, em 2012, os quatro lotes ativos de Yasuní produziram 32 milhões de barris de petróleo, 17,4% do total de 184 milhões de barris produzidos em todo o Equador naquele ano.</p>

        <p>De acordo com Eugenio Pappalardo, as áreas de concessão de petróleo representam quase 75% da área total da Reserva da Biosfera Yasuní, e incluem partes do território Waorani. Só a zona intangível é livre de atividade petrolífera.</p>

        <p>Contido na Reserva da Biosfera, estima-se que o subsolo do Parque Nacional Yasuní contenha 895 milhões de barris apenas nos campos ITT (43) e no lote 31. De acordo com um levantamento de 2010 da Secretaria de Hidrocarbonetos do Equador, isso corresponde a 12,3% das reservas comprovadas do país de 7,3 bilhões de barris.</p>

