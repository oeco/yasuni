        <p>A exploração de petróleo em Yasuní desperta críticas ferozes. A Constituição do Equador diz que atividades de extração não devem ocorrer dentro de áreas protegidas. Também estabelece que as gerações presentes e futuras têm direito à conservação e restauração da natureza. De acordo com o Ricardo Crespo, ex-assessor da Comissão de Biodiversidade da Assembleia Nacional do Equador, o problema é que a mesma Constituição também contém o artigo 407, onde surge a expressão “interesse nacional”. Esse artigo permite que o presidente da República solicite à Assembleia Nacional declarar de interesse nacional a exploração de petróleo, minério ou madeira dentro de áreas protegidas.</p>
        
        <div class="video">
            <iframe src="//player.vimeo.com/video/83624381?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>Em 2013, o presidente Rafael Correa usou o artigo 407 para declarar de interesse nacional a exploração de petróleo nos campos ITT (lote 43). Isso foi feito apesar de ter sido o próprio governo que, de 2007 a 2013, promoveu a necessidade de se respeitar os grupos indígenas em isolamento voluntário e de evitar a exploração de petróleo. A Assembleia Nacional do país aceitou o pedido do presidente.</p>
        
        <p>A mesma “constituição verde”, como já foi chamada, prioriza as culturas, comunidades, nações indígenas e povos isolados através da ratificação de instrumentos internacionais de proteção dos povos contidos no artigo 57 da declaração de Direitos Indígenas da ONU. Por isso, a Constituição deveria garantir os direitos coletivos das comunidades indígenas e dos povos em isolamento voluntário, como os grupos tagaeri e taromenane que estão dentro do Parque Nacional Yasuní.</p>

