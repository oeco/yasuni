        <p>A intervenção do Instituto Linguístico de Verano, a relocação dos indígenas e a vida sedentária levou ao aumento da população waorani, pois reduziu os homicídios entre grupos indígenas. Em menos de 30 anos os waorani quase triplicaram seu número, de 500 para 1300. Entretanto, perderam grande parte de seu território e nunca se integraram à sociedade equatoriana ocidental. Atualmente, são cidadãos pobres e vulneráveis às decisões das companhias de petróleo em seus territórios.</p>

        <p>Suas fontes de renda são derivadas de empregos em geral temporários na indústria petrolífera. Além disso, as negociações das petroleiras com as comunidades indígenas para entrar em novas áreas de pesquisa e exploração quase sempre terminam em compensações materiais e em dinheiro.</p>

        <p>O contato com as petroleiras e colonos levou os waorani a fazer do comércio da carne silvestre e do desmatamento ilegal suas principais atividades, justamente as que atentam contra o meio ambiente de seu território ancestral.</p>

