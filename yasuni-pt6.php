        <h1>Os waorani</h1>

        <p>Conhecidos como waorani ou waos são indígenas de hábitos nômades ancestrais, que se movem pela vasta selva entre os dois grandes rios da região, principalmente pela parte sul do rio Yasuní. Eles compartilham o mesmo idioma com pelo menos dois grupos, os Tagaeri e os Taromenane, povos em isolamento voluntário, também de hábitos nômades.</p>

        <p>Até os anos 70 do século passado, o território dos waorani permaneceu inexplorado, pois os indígenas respondiam com ferocidade a invasões. Em 1959, mataram cinco missionários evangélicos americanos que entraram na região do rio Cururay, encontrados com os corpos atravessados por lanças wao.</p>

        <p>Os waorani receberam dos outros grupos indígenas o nome de “aucas”, sinônimo de selvagens. Eles se autodenominam wao, que em seu idioma wao-terero significa homem. De origem Tupi, os wao eram caçadores-coletores (alguns ainda o são), que viajavam pela floresta e aproveitavam seus recursos.</p>

        <p>A vida dos waorani mudou nos anos 50, com o interesse na exploração de petróleo em Yasuní. A primeira providência do governo e empresas petrolíferas foi contatar esses povos. O Instituto Linguístico de Verano (ILV), missão evangélica americana, foi o braço desta ação. Estabeleceu relação com uma parte das famílias waorani e as levou para uma reserva (chamada de protetorado) em Toñampare, na província de Pastaza. Assim, esvaziaram a região para o início das atividades petrolíferas de empresas como Texaco, Shell e Gulf Oil.</p>

        <p>Os waorani se dividiram: um grupo decidiu ficar na reserva e outro grupo a abandonou e voltou para a selva. Entretanto, encontraram um território que já não era deles. Depararam-se com uma paisagem destruída, de habitat fragmentado e perfurada por poços de petróleo. Uma estrada levou embora a floresta nativa e trouxe migrantes mestiços de outras regiões do país, que vieram com suas roças e suas armas de fogo.</p>

