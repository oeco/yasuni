        <h2>Jaguars (or Kengiwe)</h2>

        <p>The jaguar <i>(Panthera onca)</i> is known by the name of kengiwe in the wao terero language of the Waorani tribe. According to beliefs, the kengiwe is the reincarnation of a warrior from the tribe. After death, warriors would return to the forest exhibiting a golden color, a robust body, a large head and powerful jaws.</p>

        <div class="bigimage"><img class="" style="width:700px;" src="img/fauna/jaguar.jpg" alt="Photo by Diego Mosquera">
        <p class="legend"><i>Photo by Diego Mosquera</i></p>
        </div>

        <p>Despite its strength and size, jaguars instinctively remain shy of people. However, with photographic traps, scientists have already recorded the species 250 times in the last 8 years. With these images, today it is known that 21 jaguars exist in the neighborhood of the Tiputini Biodiversity Station, in the Yasuní Biosphere Reserve.</p>

        <div class="video">
        <iframe src="//player.vimeo.com/video/82726643?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="700" height="300" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>Jaguars hunt and devour everything that moves in rivers or in woodlands. Fish, rodents and wild boars are part of a list of 80 species of animals on the diet of these felines. Their presence is a sure sign of a healthy forest, for the species requires an abundance of game to survive. Each individual needs an area close to 10,000 hectares (equivalent to 10 thousand soccer fields) to find its sustenance and conditions to reproduce.</p>

        <p>In over 8 years of research in Yasuní, Diego Mosquera, Kelly Swing, David Romo and other researchers from the University of San Francisco de Quito obtained almost 55 thousand photographs of 60 different species, among mammals, birds, amphibians and reptiles, although only 600 hectares had been studied and the ecologists had not yet finished processing all of the animal records collected.</p>

