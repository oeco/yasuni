        <h1>Hunting</h1>

        <p>In the last 40 years, the vast territory between rivers Napo and Cururay has changed. Roads invaded the area and allowed access to previously remote areas. Through these, indigenous people from the Waorani tribe and the recently arrived Kichwas indigenous people (the first indigenous group in the Ecuadorian Amazon to be catechized) hunt in locations in the forest which were once inaccessible or outside their communities. Besides, subsistence hunting was transformed into a commercial activity, making it unsustainable.</p>

        <blockquote>The Amazonian Kichwas indigenous people are the largest group within Yasuní National Park’s zone of influence. Their population grew along Napo River, as from the second half of the 19th century, during the expansion of the rubber industry.</blockquote>

        <div class="bigimage"><img style="width:800px;" src="img/via_auca/02_via_auca_full.jpg" alt="Photo by Martin R. Bustamante / www.biographica.com.ec">
        <p class="legend"><i>Photo by Martin R. Bustamante / www.biographica.com.ec</i></p>
        </div>
 
        <p>The flesh of wildlife captured in Yasuní is commercialized in Pompeya, a small township on the banks of Napo River. In marketplaces open on Saturdays, one can find the meat of wild boars, deer, turtle eggs and a variety of fish such as arapaima, curimbatá and bodós, which are sold to the people who come to the cities of Puyo, Coca and Tena.</p>

        <p>Years ago, this commerce was more evident. Repression on the part of Ecuador’s Ministry of the Environment made this activity more discreet, but also produced other commercialization points such as Chiro Isla, on the park’s northern portion.</p>

        <p>Wildlife meat is coveted because it has a ritual value for the native population of the Amazon. For tourists, on the other hand, it is regarded as a delicacy and a folkloric experience.</p>

        <div class="video">
        <iframe src="//player.vimeo.com/video/83402862?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <div class="lead">It is thought that close to 10 tons of  meat are sold illegally each year, especially derived from average-sized mammals, aside from birds and fish</div>

        <p>Ecologist Esteban Suárez, in partnership with the Wildlife Conservation Society (WCS) and the University of San Francisco de Quito, researched the market at Pompeya and managed to put the magnitude of wildlife meat commerce into numbers. It is thought that close to 10 tons are sold illegally each year, especially derived from average-sized mammals, aside from birds and fish. “The scenario is discouraging, for this meat comes especially from mammals that already have a low population density, where the impact of hunting is stronger. We are seeing capture levels that will make the population of these animals collapse in the near future”, says Suárez.</p>

        <p>The benefits remain in the city, because go-betweens and restaurants sell this meat for five times the price paid to hunters. Meanwhile, animals are vanishing in the forest.</p>

