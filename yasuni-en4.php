        <p>Ecological characteristics, a variety of habitats and geographic localization made Yasuní a cradle for the development of new species.</p>

        <p>In the words of Stella de la Torre, doctor of Zoology from the University of San Francisco de Quito, the region’s rivers frequently alter their course and form natural barriers that can separate entire populations of wildlife and plant life alike. Environments with subtle differences exert evolutionary pressures that produce these distinct species in areas that are quite close to each other.</p>

        <div class="video">
        <iframe src="//player.vimeo.com/video/82719593?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>This mosaic of habitats was formed by the range of altitudes within an equatorial region close to the Andes mountain range, a phenomenon which is not witnessed in other regions of the Amazon basin. Yasuní is also one of the Amazonian regions with the highest rate of rainfall.</p>

        <p>It is estimated that it contains more than 4 thousand species of vascular plants. Up to this moment, 138 species of vines have been described, as well as 313 epiphytes (plants such as bromeliads and lichens), 140 ferns and similar plants, aside from 2,113 species of trees and shrubs (1,813 of which have been identified and 300 yet to be identified). A study found in a single hectare in Yasuní 655 species of trees, a number similar to the one found in all forests of North America.</p>

        <blockquote>A <a href="http://www.plosone.org/article/info:doi/10.1371/journal.pone.0008767" target="_blank">study</a> found in a single hectare in Yasuní 655 species of trees, a number similar to the one found in all forests of North America.</blockquote>

        <div class="video">
        <iframe src="//player.vimeo.com/video/82104960?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>One hundred and seventy six species of mammals are known, among which more than 80 are bats. The list of birds has already reached 596 species, 40% of the total found in the Amazon basin. Amphibians and reptiles form a community of the planet’s richest herpetofauna, with 271 species. Fluvial and lacustrine (laky) systems are home to 382 species of fish, a number that exceeds the total amount found in the basin of river Mississippi, one of the three largest basins in the world. The sheer richness of insects mesmerizes specialists, who have already estimated some 100 thousand species in a single hectare.</p>

