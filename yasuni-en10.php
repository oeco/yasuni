        <div class="lead">Four main roads cross the Yasuní Biosphere Reserve to serve oil companies: via Auca, via Maxus, via Oxy (Edén Yuturi), and via Petrobras. According to estimates, each new kilometer of road openned in the tropical rainforest causes another 120 hectares of deforestation.</div>

        <h1>Via Auca</h1>

        <p>Throughout the 130 km of the highway known as via Auca, oil industry activities are evident in the daily lives of people. This paved road began construction in the 70s by Texaco, within the tropical rainforest of the territories of the Waorani people. Ever since, it has been the backbone of the oil industry in the province of Orellana. Through via Auca large trucks come and go with their cargo of new oil pipes to replace old ducts that are a leak hazard. Via Auca also favored the increase of hunting and lumber extraction.</p>

        <p>Lying to the south of the city of Francisco de Orellana, via Auca winds its way between two platforms that burn the gas that emanates from the wells. Recently, more modern equipment was installed in order to extract the more difficult oil from these fields. After 30 years now, they are close to depletion. Along the highway, one can see oil polluted lakes that form a blackened smooth surface.</p>

        <p>From north to south, via Auca crosses the provinces of Orellana and Pastaza. Its final leg crosses a region of Waorani communities to the west of the Yasuní Biosphere Reserve. Nowadays, a population of 11 thousand settlers lives in the vicinity of the highway. On the eastern side of the road, there are communities close to the border of the national park, areas which are also used by the indigenous people. Via Auca helps the fast growth of the population of migrants that gradually transform the forest into a cultivated frontier. Simultaneously, the oil industry forges ahead drilling the ground.</p>

