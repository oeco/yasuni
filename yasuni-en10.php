        </div>

        <p>Ulvio Torres lives in the community of Puma, just a few meters from a road that begins on via Auca. He arrived there with his family in 1979, lured by the offer of land made by the government at that time. In a similar manner to the peasants from Loja, Tungurahua, Bolívar and Manabí, who fled the drought in their provinces, Ulvio found a hostile land for agriculture and a cruel neighbor: oil. “When I arrived here back in 1979, a primary forest thrived along all routes and rivers were clean and alive with fish. People were healthier”, he recalls.</p>

        <p>Pipes that flow their oil load along the highway pass in front of his land. Ulvio says leaks are recurring. Today, there’s a greater degree of control, but ten years ago, he says, oil companies even accused peasants of causing leaks in order to obtain compensations. According to him, these accusations were false.</p>

        <p>Ulvio is one of the few settlers dedicated to agriculture, for most of them work for the oil companies. Those hired to clean up leaks earn about US$300 per month. Ulvio believes that they risk exposure to serious skin and stomach diseases, not to mention cancer. “Some people go to work for an oil company cleaning up [spilled] oil, and I told my buddies not to get into that sort of thing, arguing that if they [the oil companies] leak oil, they should clean it up themselves. But they go anyhow and end up sick”.</p>

        <p>To attract settlers to this region of the Amazon, the government of Ecuador promised to give 50 hectares of land in exchange that the new comers deforest half of it. This policy soon caused the region around via Auca to be ruined. “Anyone wandering along the complex of secondary roads in the via Auca territory notices that deforestation is very high”, says Eugenio Pappalardo, a doctor of human geography who has been studying Yasuní for eight years. “Via Auca has been the exploitation and productive vector of the oil industry and at the same time, of the agricultural colonization of this area”.</p>

        <p>According to the report “Biodiversity Hotspots for Conservation Priorities”, published by Nature magazine, the Auca transportation system is listed in 14th place among the major tropical deforestation fronts and is one of Yasuní’s worst problems.</p>

