        <h1>Secondary Roads</h1>

        <p>On the smaller roads that stem from via Auca, the situation is about the same: human settlements, agriculture, deforestation, contamination, oil leaks and the constant coming and going of trucks and platforms connected to oil drilling activities. Of these roads, the only one that goes up to Yasuní National Park is the Pindo-Tobeta road. This almost 35 km long secondary road reaches up to oil block 14 and crosses park borders, very close to the headwaters of Tiputini River.</p>

        <p>Since 1994, Yasuní also has to share its space with via Maxus, a highway controlled by Repsol that forges into the national park and into Waorani territory as well. Its surroundings suffer from the empty forest syndrome: the jungle seems relatively fine, but its animals are no longer there. “Although there is no deforestation, wildlife has already declined into a state of collapse and that is something we can measure”, explains Esteban Suárez, whose study on the hunting and sale of wildlife meat showed the decline of species of fauna.</p>

        <p>Via Maxus increased commercial hunting as it makes forest entry easy as well as the transportation of animals to the market in Pompeya. The road was gradually extended up to block 16 as new oil wells were opened and began to produce in this area of the park.</p>

        <p>Another highway is via Oxy, built by Occidental in 2003, located in plot 15 and exploited by Petroamazonas (Ecuador’s state owned oil company), in the park’s buffer zone. The road is used exclusively by the company for activities connected to oil drilling, as well as to shuttle its employees. Via Oxy is fundamental for oil industry activity since plot 15 is Ecuador’s most productive field with a production of 13 million barrels a year.</p>

        <p>On the southern bank of river Napo lies Chiro Isla, a small river port connected to another road, whose construction was initiated by Petrobras, the giant Brazilian state owned oil company. Located to the east of via Maxus, it is the newest road in this territory. Also known as via Petrobras, it is opposed by environmentalist groups. In 2007, they cheered when the Ecuadorian government suspended the environmental license issued for its construction due to inconsistencies between the project and preservation goals. Petrobras left and Petroamazonas discretely resumed the construction job. In 2013, via Petrobras reached 14.5 km and came up to the north bank of Tiputini River (a national park border). All appearances show that it will serve to operate ITT fields.</p>

        <p>Exploitation of the ITT plot brings forth the fear of new roads being built inside Yasuní National Park. The government denies that and says that machine and personnel transportation will take place by river and by helicopters. Authorities say no new roads will be built, but rather “ecologic trails” will be opened that will enable control over oil company operations. In plot 31, a long dirt road opened in the middle of the forest is an example of this concept, as shown on <a href="http://es.mongabay.com/news/2013/es1112-yasuni-secret-oil-road-finer.html" target="_blank">air surveillance photographs</a> taken in August 2012 by <a href="http://ivankphoto.com/" target="_blank">Ivan Kashinsky</a> and <a href="http://www.karlagachet.com/" target="_blank">Karla Gachet</a> during a National Geographic expedition.</p>

        <p>There is a possibility of building yet another road, this time in plot 43-ITT, close to the Ishpingo oil field. It concerns environmentalists since it would lie close to the intangible zone of Yasuní National Park where isolated indigenous tribes dwell.</p>

