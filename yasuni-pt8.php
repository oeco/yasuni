        <h1>Os Tagaeri-Taromenane</h1>

        <p>Dentro de Yasuní, no território ancestral waorani, há clãs em isolamento voluntário. Sãos os tagaeri, grupo derivado do mesmo tronco ancestral que os waorani, com os quais compartilham a língua, mas que se recusaram ao contato com a missão evangélica. Lá estão também os taromenane, outro grupo isolado, culturalmente similar aos waorani, mas sem vínculos de parentesco. Tagaeri é um nome que os waorani usam para chamar àqueles que não conhecem, e significa “as pessoas que caminham" ou “as pessoas do caminho”. É a gente que os waorani avistavam ao sair para caçar.</p>

        <p>Os tagaeris e taromenanes são seminômades, com uma economia baseada na coleta, caça e horticultura itinerante. Estão entre os últimos povos do mundo com uma cultura e sobrevivência ligadas à floresta tropical.</p> 

        <p>Enfrentamentos entre os waorani e os Tagaeri-Taromenane causam mortes e são um perigo para a sobrevivência dos grupos isolados. Estes conflitos são tradicionais, já que os grupos isolados costumavam lutar entre si por controle de território. A morte de membros de um clã era vingada, provocando novos conflitos. Hoje, entre as vítimas estão colonos e traficantes de madeira que invadem os territórios indígenas. Os grupos isolados são agressivos com os estranhos. Para eles, o território representa um espaço exclusivo de um grupo particular, e sua defesa é fundamental.</p>
        
        <p>Os isolados vivem ameaçados pelas pressões das indústrias extrativas em Yasuní. A diminuição radical de seu território devido à intensa atividade extrativa pode causar o fim das tribos isoladas. Elas estão cada vez mais perto das comunidades waorani e dos colonos, têm menos espaço e menos recursos, a quantidade de caça está diminuindo e os rios estão poluídos.</p>

        <p>“Nos últimos relatórios de avistamentos, fala-se de gente taromenane que estabeleceu contato com os waorani para pedir comida, ou seja, eles estão sofrendo com escassez”, comenta Milagros Aguirre, diretora da Fundação Alejandro Labaka.</p>

