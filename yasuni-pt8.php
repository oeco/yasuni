        <p>Os camponeses chegaram acreditando na promessa de terras férteis e disponíveis, mas em lugar disso encontraram densas florestas tropicais de solos pobres, habitadas por índios guerreiros e ameaçadas pela poluição do petróleo. Hoje, a população de colonos já é maior do que a indígena e é a que mais sofre com os vazamentos frequentes de petróleo. Os colonos apresentam índices altos de alcoolismo, violência doméstica e prostituição. Também carregam o estigma de serem os causadores do desmatamento das florestas tropicais, apesar de ter sido o governo que os incentivou a fazer isso.</p>

