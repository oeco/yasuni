        <p>Criada em 1999 e delimitada em 2007, dentro do parque nacional, a zona intangível de 758.051 hectares evitou a expansão petrolífera para o sul do parque. Entretanto, pode ser pequena para preservar estes povos em isolamento, pois não são conhecidos os limites territoriais e padrões de mobilidade na floresta dos povos Tagaeri-Taromenane.</p>

        <p>Grupos de defensores dos direitos humanos e ambientalistas pediram à Comissão Interamericana de Direitos Humanos (CIDH) para promover ações de proteção à vida dos povos e culturas em risco. As recomendações da CIDH foram acolhidas pelo governo equatoriano, que em 2008 criou o Plano de Medidas Cautelares, para eliminar atividades ilegais dentro da zona intangível. Entretanto, ele ainda não atingiu esse objetivo.</p>

        <h1>Os Colonos</h1>

        <p>Os anos 70 no Equador foram anos decisivos para as áreas protegidas do país; foram criadas várias delas, as maiores e mais representativas de ecossistemas vulneráveis. Ao mesmo tempo, a exploração de petróleo foi consolidada nas áreas mais importantes da Amazônia equatoriana: Cuyabeno e Yasuní.</p>

        <p>O deslocamento waorani até o protetorado criado pelo ILV facilitou para a Texaco começar a construção da via Auca, a principal estrada da região. Em paralelo, na serra e na costa, a exigência dos camponeses por terras para cultivar deu início a programas de colonização, especialmente na parte amazônica. Logo começou a migração de camponeses pelas chamadas “terras de ninguém”, onde receberam terrenos com a exigência de que a floresta deveria ser derrubada e trocada por plantações.</p>

