    <p>El Parque Nacional Yasuní tiene una historia turbulenta. En 30 años sus fronteras han cambiado varias veces en función de las necesidades de la actividad hidrocarburífera. Actualmente, dentro del parque son operados y activos los lotes 14, 15, 16 y 67.</p>

    <p>Existen dos bloques más, 31 y 43, cuya infraestructura está en construcción. Estos contienen 895 millones de barriles de petróleo, lo que corresponde a 13% de las reservas de Ecuador. El lote 43 está formado por los campos Ishpingo-Tambococha-Tiputini, conocidos por la sigla ITT. La propuesta del movimiento ambiental para una moratoria en la explotación de petróleo en el ITT dio lugar a una campaña, acogida por el gobierno, que promovió durante cinco años la no extracción de crudo en dichos campos.</p>
</article>

