    <p>En 1999 el extremo sur del parque nacional fue designado como Zona Intangible para garantizar los derechos de los pueblos Tagaeri-Taromenane. Estos grupos viven allí en aislamiento voluntario. En 2007 la Zona Intangible fue delimitada con 758.051 hectáreas. En esta zona se prohíbe la explotación de petróleo, ya que por ley es inviolable.</p>
 
