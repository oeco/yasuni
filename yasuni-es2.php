<article class="bigtext">
    <p>La región de Yasuní fue reconocida por el <a href="http://www.unesco.org/new/pt/brasilia/natural-sciences/environment/biodiversity/biodiversity/mab-programme-in-brazil/" target="_blank">Programa Hombre y Biosfera</a> de la UNESCO como Reserva de la Biosfera, con una <a href="http://www.unesco.org/mabdb/br/brdir/directory/biores.asp?mode=all&code=ECU+02" target="_blank">superficie total de 1.682.000 hectáreas</a> que incluye el Territorio Indígena Waorani y el Parque Nacional Yasuní. En la parte sur del Parque Nacional está la Zona Intangible Tagaeri Taromenane.</p>

    <p>Hasta los años 60 Yasuní estaba habitada sólo por pueblos indígenas que vivían en aislamiento voluntario. Fue el interés por la exploración de petróleo que llevó al gobierno de Ecuador y a las compañías petroleras a buscar el contacto con los indígenas. A partir de entonces, la mayoría de estas tribus fue desalojada de sus tierras.</p>

    <p>En 1979, sensibilizado por la importancia de Yasuní para la protección ambiental, el gobierno creó el Parque Nacional Yasuní que ocupa una porción de 982.000 hectáreas de la región. En 1983 fueron legalizadas como tierras indígenas waorani 66.500 hectáreas en la parte oeste del parque nacional. En 1989 la región recibió el estatus de Reserva de la Biosfera. En 1990, el gobierno ecuatoriano decretó un aumento de casi diez veces la superficie del territorio waorani, que aumentó a 612.560 hectáreas. La condición era que los waorani no se opondrían a la explotación de petróleo dentro de este territorio.</p>

