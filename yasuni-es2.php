<article class="bigtext">
    <h1>Iniciativa Yasuní ITT</h1>
    
    <p>En el año 2007 el gobierno de Rafael Correa lanzó de manera oficial la inicitaiva Yasuní ITT, una campaña que comprometía al Estado ecuatoriano a no explotar los campos petroleros Ishpingo-Tambococha-Tiputini (ITT) del lote 43 a cambio de una compensación económica por parte la comunidad internacional. Dichos campos se ubican en el extremo oriental de Ecuador, en la frontera con Perú, representan una quinta parte de la superficie del parque.</p>

    <p>El objetivo era conseguir 3600 millones de dólares, equivalentes al 50% de los recursos que percibiría el Estado en caso de extraer el crudo de los campos ITT. Con este fin se creó un fondo administrado por el Programa de las Naciones Unidas para el Desarrollo (PNUD). Este fondo se utilizaría para financiar proyectos en Ecuador, proteger la biodiversidad, mantener áreas protegidas, restaurar áreas degradadas, promover programas sociales e incentivar el desarrollo de energías renovables.</p>

    <p>La contaminación y degradación ambiental causada por la extracción de petróleo en los años 90, la amenaza a la biodiversidad y la violación a los derechos de los pueblos indígenas de la zona promovió desde los grupos ambientalistas la idea de conservar el petróleo bajo tierra. De ahí nace la Iniciativa Yasuní ITT.</p>

    <p>La Iniciativa ITT contó con el apoyo de 15 países, 20 organizaciones internacionales, 65 gobiernos de provincias y municipalidades de Ecuador y 39 alianzas con sectores empresariales nacionales y extranjeros. Sin embargo, hasta 2013 se obtuvieron promesas por sólo 376 millones de dólares, cerca del 10 % del total planificado.</p>

    <div class="video">
    <iframe src="//player.vimeo.com/video/83624381?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
    </div>

    <p>Después de varias prórrogas del plazo para obtener los aportes internacionales, el 15 de octubre de 2013, el presidente Rafael Correa declaró la finalización de la iniciativa y alegó  que el mundo había fallado a Ecuador. Sin embargo, durante el tiempo en que la iniciativa estuvo en vigor, los trabajos de construcción de instalaciones e infraestructura para la explotación de petróleo en los campos ITT continuaron, ello generó dudas respecto al cumplimiento por parte del Ecuador entre los gobiernos interesados.</p>

    <p>Tras el fracaso de la iniciativa, a pesar de la oposición de grupos ambientalistas y la sociedad civil, el gobierno anunció el inicio de operaciones en los campos ITT con una nueva promesa: sólo el 0,1% del parque nacional se verá afectado por la extracción de petróleo.</p>

    <p>Sin embargo, no es posible saber si los efectos negativos en la selva serán proporcionales a la zona explotada. La creación de caminos y áreas abiertas, por más pequeños que sean, provocan el aumento de asentamientos humanos, la caza y la tala, incluso en zonas controladas.</p>

    <p>La historia de la iniciativa internacional para recibir compensación por detener la explotación de los recursos naturales y cambiarla por la protección ambiental convirtió a Yasuní en un símbolo internacional. La idea de dejar el petróleo bajo tierra como un rechazo a una economía extractivista deberá esperar para consolidarse.</p>

    <p>El movimiento para proteger Yasuní regresó a la ciudadanía, que ahora reúnen firmas para promover un referéndum en se decida sobre la conservación o explotación de los campos petroleros ITT.</p>
</article>

