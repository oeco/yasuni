<article class="bigtext">
    <p>La capital de Ecuador, Quito, está a 250 kilómetros de la región conocida como Yasuní. Ubicada en el lado oeste de la cuenca amazónica, su proximidad con la cordillera de los Andes marca las selvas húmedas con una topografía accidentada por pequeñas colinas y quebradas, que son interrumpidas por riachuelos y pantanos que forman una red hídrica para el tránsito de dantas, capibaras, jaguares y caimanes. Los árboles más altos llegan a los 45 metros de altura que sobresalen del dosel (área alta de las copas de los árboles) cuya sombra cobija pantanos y bosques de tierra firme.</p>

    <p>Yasuní es una de las regiones más biodiversas del planeta, aún lejos de ser entendida por los científicos. Es tierra ancestral de grupos indígenas, algunos de los cuales están en aislamiento voluntario. Es también el hogar de un creciente número de colonos procedentes de otras partes de Ecuador. Además, mantiene las más importantes reservas de petróleo del país. La explotación de este recurso pone en peligro a la selva con la construcción de oleoductos y carreteras. Las obras alientan la llegada de inmigrantes y el aumento de la población conduce a la deforestación y a la reducción del número de animales salvajes.</p>
