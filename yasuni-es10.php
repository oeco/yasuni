        <div class="lead">Cuatro carreteras principales atraviesan la Reserva de Biosfera Yasuní y sirven a las petroleras: la vía Auca, la vía Maxus, la vía Oxy (Edén Yuturi) y la vía Petrobras. Según estimativas realizadas en base a análisis geográfico, cada kilómetro de carretera abierto en la selva tropical causa la deforestación de 120 hectáreas.</div>

        <h1>Vía Auca</h1>

        <p>A lo largo de los 130 km de la vía Auca la actividad petrolera marca su presencia y el día a día de las personas. Este camino pavimentado se comenzó a construir en los años 70 por Texaco, dentro de la selva tropical de los territorios del pueblo waorani. Desde entonces, es la columna vertebral de la industria petrolera en la provincia Orellana. Por la vía Auca pasan grandes camiones cargados de nuevas tuberías para reemplazar los oleoductos antiguos que ya cumplieron su vida útil y están sujetos a derrames. La vía Auca también facilitó el aumento de la caza y de la tala de árboles y la llegada de colonos.</p>

        <p>Al sur de la ciudad de Francisco de Orellana (Coca), la vía Auca sigue un camino entre dos plataformas que queman el gas que sale de los pozos. Recientemente fueron instalados equipos más modernos para poder extraer el poco petróleo que les queda estos campos que ya tienen  más de 30 años de explotación y están cerca de agotarse. A lo largo de la carretera se observan estanques con aguas contaminadas por el petróleo que forman un espejo negro.</p>

        <p>De norte a sur, la vía Auca atraviesa las provincias Orellana y Pastaza. Su tramo final cruza una región de comunidades waorani al oeste de la Reserva de la Biosfera Yasuní. Hoy, alrededor de la carretera hay una población de 11.000 colonos. Del lado este del camino hay comunidades cercanas al límite del parque nacional, áreas que también son utilizadas por los indígenas. La vía Auca facilita el rápido crecimiento de la población de migrantes que está transformando la selva en zonas deforestadas. Al mismo tiempo, la industria del petróleo sigue perforando el suelo.</p>

