        <p>Ulvio Torres vive en la comunidad Puma, a pocos metros de un camino que comienza en la vía Auca. Llegó con su familia en 1979 atraído por la oferta de tierras hecha por el gobierno de la época. Del mismo modo que los campesinos de Loja, Tungurahua, Bolívar y Manabí, huyendo de la sequía en sus provincias, Ulvio encontró una tierra hostil para la agricultura y un cruel vecino: el petróleo. “Cuando llegué en 1979, el bosque era primario en todas las vías, los ríos estaban limpios y abundantes en peces. Las personas eran más saludables”, asegura.</p>

        <p>Los ductos que transportan el petróleo a lo largo de la carretera pasan frente a su casa. Ulvio dice que los derrames son recurrentes. Hoy hay un poco más de control, pero hace diez años las compañías petroleras llegaban a acusar a los campesinos de causar las fugas para obtener compensación. Según Ulvio las acusaciones eran falsas.</p>

        <p>Ulvio es uno de los pocos colonos que se dedica a la agricultura ya que la gran mayoría trabaja para las petroleras. Las personas contratadas para limpiar derrames reciben alrededor de US$ 300 por mes. Ulvio cree que se exponen a graves enfermedades de la piel, del estómago y a cánceres. “Hay personas que trabajan en una petrolera juntando petróleo [derramado], yo le dije a mis compañeros que no lo hagan, que así como ellos [las petroleras] derraman, que ellos mismos limpien el petróleo. Pero la gente va y termina enferma”.</p>

        <p>Recibir 50 hectáreas de tierra y, a cambio, desmontar la mitad, era la promesa del gobierno de Ecuador para atraer colonos a esta región de la Amazonia. Esta política hizo que en poco tiempo fuera deforestado el entorno de la vía Auca. “Cualquier persona que camina a través de las carreteras secundarias en el territorio de la vía Auca se da cuenta que la deforestación es muy alta”, dice Eugenio Pappalardo, doctor en geografía humana que hace ocho años realiza estudios sobre Yasuní. “La vía Auca ha sido el verdadero vector exploratorio y productivo de la industria del petróleo y al mismo tiempo de la colonización agrícola de esta zona”.</p>

        <p>Según el informe Biodiversity Hotspots for Conservation Priorities, publicado en la revista Nature, el sistema vial de la carretera Auca está en el puesto 14 entre los principales frentes de deforestación tropical y es uno de los problemas que afetan a Yasuní.</p>

