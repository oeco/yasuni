        <h1>Carreteras Secundarias</h1>

        <p>En las carreteras secundarias que parten de la vía Auca la situación es similar: asentamientos humanos, agricultura, deforestación, contaminación, derrames y el paso constante de camiones, maquinaria y plataformas relacionadas con la actividad petrolera. De estas vías la única que llega hasta el Parque Nacional Yasuní es la Pindo-Tobeta. Este largo camino secundario de casi 35 kilómetros llega hasta el Lote 14 y cruza el límite del parque, muy cerca de las cabeceras del río Tiputini.</p>

        <p>Yasuní convive desde 1994 con la vía Maxus, una carretera controlada por la compañía Repsol que entra al parque nacional y al territorio waorani. Sus alrededores sufren el síndrome del bosque vacío: la selva parece relativamente conservada, pero los animales ya no están ahí. “Aunque no hay deforestación, las poblaciones de animales están colapsando”, explica Esteban Suárez, cuyo estudio sobre la caza y la venta de carne de animales silvestres mostró una disminución de las especies de fauna.</p>

        <p>La vía Maxus aumentó la caza comercial, ya que facilita el ingreso al bosque y el transporte de los animales al mercado de Pompeya. El camino se extendió gradualmente a lo largo del lote 16 según se fueron abriendo nuevos pozos y comenzaron a producir petróleo en esta parte del parque.</p>

        <p>Otra carretera es la vía Oxy, construida por la compañía Occidental en 2003, y ubicada en el lote 15, operado por Petroamazonas, en la zona de amortiguamiento del parque. El camino es usado exclusivamente por la empresa para actividades relacionadas con la exploración de petróleo, como el tránsito de sus empleados. La ruta Oxy es fundamental para la actividad petrolera, ya que el lote 15 es el más productivo del Ecuador, con una producción de 13 millones de barriles por año.</p>

        <p>En la orilla sur del río Napo está Chiro Isla, pequeño puerto fluvial conectado a otra carretera, cuya construcción fue iniciada por Petrobras. Situada al este de la vía Maxus, es la más nueva carretera en este territorio. También llamada vía Petrobras, es rechazada por grupos ambientalistas. En 2007, estos grupos celebraron cuando el gobierno ecuatoriano suspendió la licencia ambiental para su construcción por inconsistencias entre el proyecto y los objetivos de conservación. Petrobras salió de escena y Petroamazonas (empresa petrolera estatal de Ecuador) discretamente retomó la obra. En 2013, la vía Petrobras alcanzó 14,5 kilómetros y llegó a la orilla norte del río Tiputini (límite del parque nacional). Todo indica que la vía servirá para operar los campos ITT.</p>

        <p>La operación del lote ITT hace temer por la construcción de nuevos caminos dentro del Parque Nacional Yasuní. El gobierno lo niega y dice que el movimiento de la maquinaria y el transporte de personas serán realizados por vía fluvial y por helicópteros. Las autoridades dicen que no serán construidas nuevas carreteras, pero sí se abrirán “senderos ecológicos”, que permitirán el control de las operaciones petroleras. En el lote 31, un largo camino de tierra en medio de la selva es un ejemplo de esta contradicción, como muestran <a href="http://es.mongabay.com/news/2013/es1112-yasuni-secret-oil-road-finer.html" target="_blank">fotos aéreas</a> tomadas en agosto de 2012 por <a href="http://ivankphoto.com/" target="_blank">Ivan Kashinsky</a> y <a href="http://www.karlagachet.com/" target="_blank">Karla Gachet</a> durante una expedición de National Geographic.</p>

        <p>Existe la posibilidad de la construcción de otra carretera, esta vez en el lote 43-ITT, cerca del campo petrolero Ishpingo. Esto preocupa a ambientalistas porque sería cerca de la zona intangible del Parque Nacional Yasuní, donde viven los indígenas aislados.</p>

