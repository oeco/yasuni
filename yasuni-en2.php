<article class="bigtext">
    <h1>The Yasuní-ITT Initiative</h1>
    
    <p>In August 2010, at the beginning of Rafael Correa’s presidential term, the government launched a plan in which it committed itself to abstain from exploiting oil from the Ishpingo-Tambococha-Tiputini (ITT) oil fields, from plot 43, located within Yasuní National Park, as long as it were financially compensated by the international community. This plan became known as the Yasuní-ITT Initiative.</p>

    <p>The goal was to obtain 3.6 billion dollars, or half the appraised value of the ITT oil reserve fields. After the official launch, in order to collect the cash, a fund was devised and managed by the United Nations Development Programme (UNDP). The resources from this fund would be destined to finance projects in Ecuador to protect biodiversity, upkeep protected areas and restore degraded areas. It would also be used for social programs and for the development of renewable sources of energy.</p>

    <p>The Yasuní-ITT Initiative was born from environmentalist groups concerned with the growing pollution caused by oil extraction activities in the 90s and also to defend the rights of indigenous people. ITT fields are located in Ecuador’s easternmost region, on the border with Peru, and cover an area of 200 thousand hectares. They represent 20% of the total surface of Yasuní National Park.</p>

    <p>The ITT Initiative counted on the support of 15 countries, 20 international entities, 65 governments from provinces and municipalities in Ecuador, aside from 39 alliances with domestic and foreign business sectors. However, up to 2013, it only received promises of 376 million dollars, or 10% of the desired total.</p>

    <div class="video">
    <iframe src="//player.vimeo.com/video/83624381?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
    </div>

    <p>After several deadline extensions in order to receive international funds, on October 15, 2013, President Rafael Correa declared the initiative closed.</p>

    <p>When he abandoned the Yasuní-ITT Initiative, President Rafael Correa stated that the world had failed on Ecuador. However, during the period in which the ITT Initiative was in full force, the government continued with installations and infrastructure construction works for the exploitation of oil within ITT fields, a fact that generated doubts amongst interested countries, such as Germany, whether Ecuador would fulfill its part of the agreement.</p>

    <p>After the plan’s demise, despite opposition from environmentalists, the government announced the beginning of exploitation in ITT fields with a new promise: only 0.1% of the national park would be affected by oil extraction.</p>

    <p>However, it is not possible to know whether the negative effects on the forest will be proportional to the area exploited. The creation of roads and cleared areas, however small they may be, carry an increase of human settlements, hunting and lumber extraction, including protected areas.</p>

    <p>The story of the IIT Initiative transformed Yasuní into an international symbol. But what should’ve been a new model for preservation, died out before the ink dried on the official documents, so to speak.</p>

    <p>The movement to protect Yasuní returned to the environmental groups that are now busy amassing signatures from civil society to promote a citizen consultation, in which inhabitants will decide on the preservation or exploitation of oil in ITT fields.</p>
</article>

