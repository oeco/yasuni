    <p>The Yasuní National Park itself has a troubled history. Over 30 years, its borders have changed several times due to the needs of oil exploitation. Today, within its boundaries, oil blocks named 14, 15, 16 and 67 are active and operational.</p>

    <p>There are also two others, blocks 31 and 43 that are still undergoing infrastructure construction work for exploitation. Alone, they amass 895 million oil barrels that correspond to one-third of Ecuador’s known reserves. Block 43 contains the fields of Ishpingo-Tambococha-Tiputini, better known for its initials, ITT. The proposal from the environmental movement to put a moratorium on oil exploitation in ITT fields gave birth to one of the world’s most original and bold conservation programs.</p>
</article>

