<article class="bigtext">
    <p>The region of Yasuní was acknowledged by UNESCO’s <a href="http://www.unesco.org/new/pt/brasilia/natural-sciences/environment/biodiversity/biodiversity/mab-programme-in-brazil/" target="_blank">Man and the Biosphere Programme (MAB)</a> as a Biosphere Reserve, an <a href="http://www.unesco.org/mabdb/br/brdir/directory/biores.asp?mode=all&code=ECU+02" target="_blank">area that covers 1,682,000 hectares</a>, that includes the Indigenous Territory of the Waorani and the Yasuní National Park. Also, contained in National Park’s southern portion, lies the Intangible Zone of Tagaeri-Taromenane.</p>

    <p>Up to the 1960s Yasuní was inhabited only by indigenous peoples living in isolation. A keen interest in oil exploitation was what drew both the government of Ecuador and oil companies to approach these indigenous tribes. Subsequently, a large portion of these populations was evicted from their lands.</p>

    <p>By 1979, sensitized by the importance of Yasuní for environmental protection, the government created a national park that covered an area of 982,000 hectares of this region. In 1983, 66,500 hectares to the west of the park’s estate were legalized as indigenous lands belonging to the Waorani. In 1989, the region was awarded the status of Biosphere Reserve. In 1990, the Ecuadorian government decreed an increase of almost ten times the original area reserved for the Waorani, reaching 612,560 hectares. The condition imposed on this concession was that they were not to oppose themselves to the exploitation of oil and gas within this territory.</p>

