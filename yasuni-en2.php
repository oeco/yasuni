<article class="bigtext">
    <p>Ecuador’s capital city, Quito, lies at 250 km from the region known as Yasuní, the name of one of the rivers that cross it. Located to the west of the Amazon basin, its proximity to the Andes mountain range results in a rangy topography of hillocks and gorges for its rainforests. These are crisscrossed by creeks and swamps that form a hydric network where tapirs, capybaras, jaguars and crocodiles roam. The tallest trees reach 45 meters into the sky and stand out from the rest of the forest’s canopy, a high up area where branches and leaves overlap to produce shade for swamps and a lush underbrush.</p>

    <p>Yasuní has one of the planet’s richest biodiversity, still far from being understood by scientists. It is the ancient land of groups of indigenous people, some of which have chosen to live in isolation. It is also home to a growing number of settlers that arrive from other parts of Ecuador. Moreover, it holds Ecuador’s most important oil reserves. The exploitation of this resource threatens the forest with the building of pipelines and roads. These, in turn, encourage the arrival of more migrants, increasing its population, while also leading to deforestation and the reduction of wildlife.</p>
</article>

