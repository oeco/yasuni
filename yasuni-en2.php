    <p>In 1999, the national park’s southern portion was defined as an Intangible Zone in order to ensure the rights of the Tagaeri-Taromenane people that live there in voluntary isolation. In 2007, the Intangible Zone was marked off at 758,051 hectares and officially handed over to its indigenous inhabitants. Within its borders there is no oil exploitation for, by law, it is untouchable.</p>
 
