    <article class="bigtext">

        <p>As características ecológicas, variedade de habitats e localização geográfica fizeram de Yasuní um berço para o desenvolvimento de novas espécies.</p>

        <p>Como explica a doutora em Zoologia, Stella de la Torre, da Universidade San Francisco de Quito, os rios da região mudam de curso com frequência e formam barreiras naturais que podem separar populações de fauna e flora. Ambientes com diferenças sutis exercem pressões evolutivas que produzem espécies distintas em áreas muito próximas.</p>

        <div class="video">
        <iframe src="//player.vimeo.com/video/82719593?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>Este mosaico de habitats se formou pelo gradiente de altitudes em uma região equatorial próxima aos Andes, um fenômeno não visto em outras regiões da bacia amazônica. Yasuní é também uma das regiões amazônicas com maior incidência de chuvas.</p>

        <p>Estima-se que contenha mais de 4 mil espécies de <a href="http://pt.wikipedia.org/wiki/Planta_vascular" target="_blank">plantas vasculares</a>. Até agora foram descritas 138 espécies de cipós, 313 de epífitas (plantas como bromélias ou musgos), 140 samambaias e afins, além de 2.113 espécies de árvores e arbustos (1.813 identificadas e 300 a identificar). Uma pesquisa encontrou em um único hectare em Yasuní 655 espécies de árvores, a mesma quantidade encontrada em todas as florestas da América do Norte.</p>

        <blockquote>Uma <a href="http://www.plosone.org/article/info:doi/10.1371/journal.pone.0008767" target="_blank">pesquisa</a> encontrou em um único hectare em Yasuní 655 espécies de árvores, a mesma quantidade encontrada em todas as florestas da América do Norte.</blockquote>

        <div class="video">
        <iframe src="//player.vimeo.com/video/82104960?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>Conhece-se 176 espécies de mamíferos, dentre os quais mais de 80 morcegos. A lista de aves registra 596 espécies, 40% do total da bacia amazônica. Os anfíbios e répteis formam a comunidade de herpetofauna mais rica do planeta, com 271 espécies. Os sistemas fluviais e lacustres (referente a lagos) abrigam 382 espécies de peixes, número que supera o total de toda a bacia do rio Mississippi, uma das três maiores do mundo. A riqueza de insetos enlouquece os especialistas, que já estimaram cerca de 100 mil espécies em um único hectare.</p>

