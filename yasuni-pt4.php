        <h2>Onça (ou kengiwe)</h2>

        <p>A onça <i>(Panthera onca)</i> é chamada de kengiwe na língua wao terero dos indígenas waorani. De acordo com as crenças, o kengiwe é a reencarnação de um guerreiro da tribo. Após a morte, os guerreiros retornariam à floresta com a cor do ouro, corpos robustos, cabeças grandes e poderosas mandíbulas.</p>

        <div class="bigimage"><img class="" style="width:700px;" src="img/fauna/jaguar.jpg" alt="Foto: Diego Mosquera">
        <p class="legend"><i>Foto: Diego Mosquera</i></p>
        </div>

        <p>Apesar da força e tamanho, por instinto as onças se mantêm afastadas das pessoas. Porém, com armadilhas fotográficas, os cientistas já as fotografaram 250 vezes nos últimos 8 anos. Com essas imagens, hoje se sabe da existência de 21 onças nos arredores da Estação de Biodiversidade Tiputini, na Reserva da Biosfera Yasuní.</p>

        <div class="video">
        <iframe src="//player.vimeo.com/video/82726643?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="700" height="300" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <p>As onças caçam no rio ou no bosque. Peixes, roedores e porcos selvagens fazem parte de uma lista de 80 espécies de animais dos quais se alimentam. Sua presença é um indicador ambiental da saúde da floresta, pois a espécie precisa de abundância de caça para sobreviver. Cada indivíduo precisa de uma área próxima de quase 10.000 hectares para encontrar alimento, esconderijo e se reproduzir.</p>

        <p>Em mais de 8 anos de pesquisa em Yasuní, Diego Mosquera, Kelly Swing, David Romo e outros pesquisadores da Universidade San Francisco de Quito conseguiram quase <a href="http://amazonianwildlife.blogspot.com/p/proyecto-camaras" target="_blank">55 mil fotografias</a> de 60 espécies diferentes, entre mamíferos, aves, anfíbios e répteis,  embora só 600 hectares tenham sido estudados e os ecólogos ainda não tenham terminado de processar todos os registros de animais.</p>

