        <h1>Caça</h1>

        <p>Nos últimos 40 anos o grande território entre os rios Napo e Cururay se transformou. As estradas avançaram e permitiram acesso a lugares antes remotos. Através delas, os índios waorani e os recentemente chegados índios kichwas (primeiro grupo indígena da Amazônia equatoriana a ser catequizado) caçam em locais da floresta antes inacessíveis, fora das suas comunidades. Além disso, a caça de subsistência foi transformada em atividade comercial, o que a tornou insustentável.</p>

        <blockquote>Os índios kichwas amazônicos são o grupo de maior número na zona de influência do Parque Nacional Yasuní. Sua população cresceu ao longo do rio Napo, a partir da segunda metade do século XIX, pela expansão da indústria da borracha.</blockquote>

        <div class="bigimage"><img style="width:800px;" src="img/via_auca/02_via_auca_full.jpg" alt="Foto: Martin R. Bustamante / www.biographica.com.ec">
        <p class="legend"><i>Foto: Martin R. Bustamante / www.biographica.com.ec</i></p>
        </div>
 
        <p>A carne de animais silvestres capturados em Yasuní é comercializada em Pompeya, pequeno povoado nas margens do rio Napo. Na feira dos sábados dá para encontrar porcos selvagens, veados, ovos de tartaruga e uma grande variedade de peixes como pirarucu, curimbatá e bodós, que são vendidos às pessoas que vêm das cidades de Puyo, Coca e Tena.</p>

        <p>Anos atrás este comércio era mais evidente. A repressão do Ministério do Ambiente do Equador tornou a atividade mais discreta, entretanto também fez surgir outros pontos de venda, como em Chiro Isla, na parte norte do parque.</p>

        <p>A carne silvestre é cobiçada por que tem um valor ritual para a população nativa da Amazônia. Para os turistas, é considerada uma iguaria e uma experiência folclórica.</p>

        <div class="video">
        <iframe src="//player.vimeo.com/video/83402862?title=0&amp;byline=0&amp;portrait=0&amp;color=ffffff" width="800" height="343" frameborder="0" webkitallowfullscreen mozallowfullscreen allowfullscreen></iframe>
        </div>

        <div class="lead">Estima-se que a cada ano são vendidas ilegalmente 10 toneladas de carne silvestre, principalmente de mamíferos de tamanho médio, além de aves e peixes</div>

        <p>O ecólogo Esteban Suárez, em parceria com a Wildlife Conservation Society (WCS) e a Universidad San Francisco de Quito, pesquisou o mercado de Pompeya e conseguiu quantificar a magnitude do comércio da carne silvestre. Estima-se que a cada ano são vendidas ilegalmente 10 toneladas, principalmente de mamíferos de tamanho médio, além de aves e peixes. “O panorama não é animador, pois essa carne vem especialmente de mamíferos com populações de densidades baixas, onde o impacto da caça é forte. Estamos verificando níveis que farão as populações destes animais entrarem em colapso em um futuro próximo”, diz Suárez.</p>

        <p>Os benefícios ficam na cidade, pois os intermediários e os restaurantes vendem a carne por um valor cinco vezes maior do que o preço pago ao caçador. Enquanto isso, na floresta os animais desaparecem.</p>

